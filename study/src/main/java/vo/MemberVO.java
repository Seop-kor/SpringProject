package vo;

public class MemberVO {
    int code;
    String mid;
    String mpass;
    String mpass_check;
    String mname;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMid() {
        return mid;
    }

    public void setMid(String mid) {
        this.mid = mid;
    }

    public String getMpass() {
        return mpass;
    }

    public void setMpass(String mpass) {
        this.mpass = mpass;
    }

    public String getMpass_check() {
        return mpass_check;
    }

    public void setMpass_check(String mpass_check) {
        this.mpass_check = mpass_check;
    }

    public String getMname() {
        return mname;
    }

    public void setMname(String mname) {
        this.mname = mname;
    }
}
